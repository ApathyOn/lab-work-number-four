"""Простой модуль задержки для паузы между заданиями.
Ожидает нажатие Enter пользователем.
"""

import sys

def pause():
    """Ожидает, пока пользователь нажмёт Enter.
    Если пользователь нажимает Ctrl+C — программа завершится корректно.
    """
    print('\n--- Нажмите Enter, чтобы продолжить ---')
    try:
        input()
    except KeyboardInterrupt:
        print('\nВыход.')
        sys.exit(0)
